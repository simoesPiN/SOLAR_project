
public class Inclination {
	
	private double latitude;
	private String season;
	private int day;
	
	double getLatitude() {
		return latitude;
	}
	void setLatitude(double latitude) {
		this.latitude = latitude;
	}
	
	String getSeason() {
		return season;
	}
	void setSeason(String season) {
		this.season = season;
	}
	
	int getDay() {
		return day;
	}
	void setDay(int day) {
		this.day = day;
	}

}
